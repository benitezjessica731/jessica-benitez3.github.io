function countChars(str, char) {
  //create a let called count and set equal to 0
  let count = 0;
  //create a foor loop to iterate over str
  for (let i = 0; i < str.length; i++) {
    //create if statement to determine if any char are equal to str[i]
    if (str[i] === char) {

      count++;
    }
  }
  return count;
}

